''' 2-1. 사칙연산 '''
# 계산 결과를 출력
print(4+4)
print(4-4)
print(4*4)
print(4/4)
print((4+4)*4) #우선 순위 연산 가능
print(4**4) # a**b ; a의 b제곱
print(5//4) # a//b ; a나누기 b의 몫
print(5%4) # a%b ; a나누기 b의 나머지
# 변수 연산
number = 4
print(number)
number += 4 # number = number + 4 와 동일
print(number)
number -= 4 # number = number - 4 와 동일
print(number)
number /= 4 # number = number / 4 와 동일
print(number)
number *= 4 # number = number * 4 와 동일
print(number)
number **= 4 # number = number ** 4 와 동일
print(number)
number //= 4 # number = number // 4 와 동일
print(number)
number %= 4 # number = number % 4 와 동일
print(number)